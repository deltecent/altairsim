	ORG	0000H

WAIT	EQU	0FCH
SECT	EQU	0FAH
DCOM	EQU	0F8H
DDATA	EQU	0FBH
DSTAT	EQU	0F8H

BOOT:	IN	WAIT		;WAIT FOR HOME.
	XRA	A		;COMPLETE.
	MOV	L,A		;SET L=0.
	MOV	H,A		;HAL=0.
	INR	A		;SET A=1.
	OUT	SECT		;SECTOR = 1.
	MVI	A,0CH		;READ SECTOR.
	OUT	DCOM
RLOOP:	IN	WAIT		;WAIT FOR DRQ OR INTRQ.
	ORA	A		;SET FLAGS.
	JP	RDONE		;DONE IF INTRQ.
	IN	DDATA		;READ A BYTE OF DATA.
	MOV	M,A		;PUT INTO MEMORY.
	INX	H		;INCREMENT POINTER.
	JMP	RLOOP		;DO IT AGAIN.
RDONE:	IN	DSTAT		;READ DISK STATUS.
	ORA	A		;SET FLAGS.
	JZ	07DH		;IF ZERO, GO TO SBOOT.
	HLT

